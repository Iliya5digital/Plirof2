<li><font ><font >12</font></font><del><font ><font >.</font></font></del><font ><font >Το 3789000 εμφανίζεται ως </font></font><code>1.23789E+08</code><font ><font >&nbsp;(δοκιμάστε να μετακινήσετε τα κενά κόμματα 8 στα αριστερά)</font></font></li>
